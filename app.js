var fila = [1,2,3,4];

fila.push(5); // agrega el elemento al final
document.write(fila);

fila.shift(); // quita el primer elemento del arreglo
document.write(fila);

fila.push('hola');
document.write(fila);

fila.unshift('perrito'); // agrega un elemento al principio del arreglo
document.write(fila);
